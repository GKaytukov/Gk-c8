const animal = 'sloth'

switch (animal) {
    case 'dog':
        console.log('woof woof')
        break
    case 'cat':
        console.log('meow meow')
        break
    case 'bird':
        console.log('cheep cheep')
        break
    case 'snake':
        console.log('hiss hiss')
        break
    default:
        console.log('I do not know that animal.')
        break

}